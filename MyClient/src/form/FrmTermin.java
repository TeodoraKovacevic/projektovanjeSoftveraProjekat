    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
try {
    
        String dan=(String) txtDan.getText();
        if(dan.equals("nedelja") || dan.equals("Nedelja")){
        JOptionPane.showMessageDialog(this, "Nedeljom ne radi skola");
        return;
        }
        if(!dan.equalsIgnoreCase("ponedeljak") && !dan.equalsIgnoreCase("utorak") && !dan.equalsIgnoreCase("sreda") && !dan.equalsIgnoreCase("cetvrtak") && !dan.equalsIgnoreCase("petak") && !dan.equalsIgnoreCase("subota") ){
        JOptionPane.showMessageDialog(this, "Neispravno unet dan");
        return;
        }
        String vreme=txtVreme.getText();
        Grupa g=(Grupa) cmbGrupa.getSelectedItem();
        int id=Integer.valueOf(txttId.getText());
        
        Jezik j=g.getJezik();
        Termin te=new Termin(id, dan, vreme, g);
        List<Termin> termini=communication.Communication.getInstance().vratiTermine();
        for(Termin ter:termini){
        if(ter.getDan().equals(te.getDan()) && ter.getVreme().equals(te.getVreme())){
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti termin!");
            return;
        }
        }
            communication.Communication.getInstance().izmeniTermin(te);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio termin");
            fpp.osvezi(te);
            
           } catch (ClassException e) {
            JOptionPane.showMessageDialog(this, "Soket je zatvoren " + e.getMessage());
            this.dispose(); // TODO add your handling code here:
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da sacuva termin"+ex.getMessage());
            System.exit(0);
                    
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

