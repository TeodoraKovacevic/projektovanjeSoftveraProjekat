    private void napuniComboBox() {
        try {
            Request request = new Request(Operation.VRATI_GRUPE, FRAMEBITS);

            List<Grupa> grupe = communication.Communication.getInstance().vratiGrupe();
            for (Grupa g : grupe) {
                cmbGrupa.addItem(g);
            }
            } catch (ClassException e) {
            JOptionPane.showMessageDialog(this, "Soket je zatvoren " + e.getMessage());
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(FrmKreiranjeProfila.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniJezik(Grupa g) throws Exception {
        try{
        Jezik j=communication.Communication.getInstance().vratiJezikPoUslovu(g.getJezik());
        System.out.println("usao u popuni jezik");
        System.out.println(j);
        t.getGrupa().setJezik(j);
        txtJez.setText(j.getNaziv());
        } catch (ClassException e) {
            JOptionPane.showMessageDialog(this, "Soket je zatvoren " + e.getMessage());
            this.dispose();
        }
    }
